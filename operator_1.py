#Nhap so nguyen 
a = int(input("Số nguyên thứ nhất: "))
b = int(input("Số nguyên thứ hai: "))

tong = a + b
print("Tổng hai số nguyên là: ", tong)

hieu = a - b
print ("Hieu hai so nguyen la: ", hieu)

nhan = a*b 
print("Tích hai số nguyên: ", nhan)

chia = a-b
print("Thương hai số nguyên ", chia)

chia_lay_du = a%b
print("chia lấy phần dư của 2 số nguyên là:", chia_lay_du)

chia_lay_nguyen = a//b
print("chia lấy phần nguyên của 2 số là: ", chia_lay_nguyen)
